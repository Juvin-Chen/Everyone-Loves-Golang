-- 1. 查看表相关语句


-- 1.1 查看当前数据库中所有的表名
SHOW TABLES;

-- 1.2 查看表的结构（字段名、类型、是否为空、键等）
DESC 你的表名;
-- 或者写全称：DESCRIBE 你的表名;

-- 1.3 查看建表语句（查看完整的建表 SQL，包括引擎、字符集、索引定义）
SHOW CREATE TABLE 你的表名;

-- 1.4 查看表的索引信息
SHOW INDEX FROM 你的表名;

-- 1.5 查看表的状态（查看引擎、行数、数据大小、创建时间等详细信息）
SHOW TABLE STATUS LIKE '你的表名';

-- 1.6 查看表的所有字段详细信息（比 DESC 更详细，包含注释、排序规则等）
SHOW FULL COLUMNS FROM 你的表名;

-- 1.7 快速预览表中的数据（查看前 10 条）
SELECT * FROM 你的表名 LIMIT 10;



-- 2. 删除表与清空数据相关语句


-- 2.1 删除整张表（表结构和数据彻底消失，操作不可逆）
DROP TABLE 你的表名;

-- 2.2 安全删除（如果表存在则删除，不存在也不报错）
DROP TABLE IF EXISTS 你的表名;

-- 2.3 清空表数据（保留表结构，删除所有数据，重置自增 ID，速度快，不可回滚）
TRUNCATE TABLE 你的表名;

-- 2.4 删除表中指定数据（保留表结构，只删除符合条件的数据，可回滚）
DELETE FROM 你的表名 WHERE id = 1;
-- 注意：如果不加 WHERE 条件，会删除全表所有数据！